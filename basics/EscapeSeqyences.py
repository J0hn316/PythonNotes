# Examples

weather = "It's kind of sunny"
weather1 = "It\'s \"kind of\" sunny"

# To add a Tab you will use  the escape character \t. 
weather2 =  "\tIt\'s \"kind of\" sunny"

# To print on a new line you will use escape character \n. 
weather3 =  "It\'s \"kind of\" sunny\n Today is Monday."

print(weather)
print(weather1)
print(weather2)
print(weather3)